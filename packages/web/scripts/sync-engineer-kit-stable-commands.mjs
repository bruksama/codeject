import { writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const SOURCE_URL = 'https://www.vividkit.dev/vi/guides/commands';
const OUTPUT_PATH = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  '../src/lib/engineer-kit-stable-commands.ts'
);
const CARD_MARKER = '<div class="p-3 rounded-lg bg-slate-50';

const HTML_ENTITY_MAP = {
  '&amp;': '&',
  '&#39;': "'",
  '&quot;': '"',
  '&lt;': '<',
  '&gt;': '>',
  '&nbsp;': ' ',
};

async function main() {
  const html = await fetchSourceHtml();
  const commands = extractStableEngineerKitCommands(html);

  if (commands.length === 0) {
    throw new Error('No stable Engineer Kit commands were extracted from the source page.');
  }

  await writeFile(OUTPUT_PATH, buildManifestSource(commands), 'utf8');
  console.info(`Wrote ${commands.length} stable Engineer Kit commands to ${OUTPUT_PATH}`);
}

async function fetchSourceHtml() {
  const response = await fetch(SOURCE_URL);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${SOURCE_URL}: ${response.status} ${response.statusText}`);
  }

  return response.text();
}

function extractStableEngineerKitCommands(html) {
  const startIndex = html.indexOf('id="categories-engineer"');
  const endIndex = html.indexOf('<!-- Official docs link -->', startIndex);

  if (startIndex === -1 || endIndex === -1) {
    throw new Error('Engineer Kit commands section was not found in the source page.');
  }

  const section = html.slice(startIndex, endIndex);
  const commands = [];
  const seenIds = new Set();

  for (const card of extractStableCards(section)) {
    if (card.includes('/ckm:') || card.includes('>beta<')) {
      continue;
    }

    const idMatch = card.match(/<code[^>]*>\/(ck:[^<\s]+)[\s\S]*?<\/code>/);
    if (!idMatch) {
      continue;
    }

    const id = idMatch[1];
    if (seenIds.has(id)) {
      continue;
    }

    const paragraphMatches = [...card.matchAll(/<p class="([^"]+)"[^>]*>([\s\S]*?)<\/p>/g)].map(
      (match) => ({
        className: match[1],
        text: decodeHtml(match[2]),
      })
    );

    const title =
      paragraphMatches.find((entry) => entry.className.includes('text-xs font-medium'))?.text ?? id;
    const description =
      paragraphMatches
        .filter((entry) => entry.className.includes('text-[11px]'))
        .map((entry) => entry.text)
        .pop() ?? '';
    const argsHint = [...card.matchAll(/<code class="text-\[9px\][^>]*>([\s\S]*?)<\/code>/g)]
      .map((match) => decodeHtml(match[1]))
      .filter(Boolean)
      .join(' ');

    commands.push({
      id,
      title,
      description,
      ...(argsHint ? { argsHint } : {}),
    });
    seenIds.add(id);
  }

  return commands.sort((left, right) => left.id.localeCompare(right.id));
}

function extractStableCards(section) {
  const cards = [];
  let searchIndex = 0;

  while (true) {
    const cardStart = section.indexOf(CARD_MARKER, searchIndex);
    if (cardStart === -1) {
      break;
    }

    const cardEnd = findClosingDiv(section, cardStart);
    if (cardEnd === -1) {
      throw new Error('Stable Engineer Kit command card parsing failed.');
    }

    cards.push(section.slice(cardStart, cardEnd));
    searchIndex = cardEnd;
  }

  return cards;
}

function findClosingDiv(source, startIndex) {
  let depth = 0;

  for (let index = startIndex; index < source.length; index += 1) {
    if (source.startsWith('<div', index)) {
      depth += 1;
      continue;
    }

    if (source.startsWith('</div>', index)) {
      depth -= 1;
      if (depth === 0) {
        return index + '</div>'.length;
      }
    }
  }

  return -1;
}

function decodeHtml(value) {
  return value
    .replace(/<[^>]+>/g, ' ')
    .replace(/&(amp|#39|quot|lt|gt|nbsp);/g, (entity) => HTML_ENTITY_MAP[entity] ?? entity)
    .replace(/\s+/g, ' ')
    .trim();
}

function buildManifestSource(commands) {
  return `// Generated by packages/web/scripts/sync-engineer-kit-stable-commands.mjs
// Source: ${SOURCE_URL}
// Manual sync only. Do not edit by hand.

export interface EngineerKitStableCommand {
  id: string;
  title: string;
  description: string;
  argsHint?: string;
}

export const engineerKitStableCommands = [
${commands.map((command) => serializeCommand(command)).join(',\n')}
] as const satisfies readonly EngineerKitStableCommand[];
`;
}

function serializeCommand(command) {
  const lines = [
    `  {`,
    `    id: ${serializeString(command.id)},`,
    `    title: ${serializeString(command.title)},`,
    `    description: ${serializeString(command.description)},`,
  ];

  if (command.argsHint) {
    lines.push(`    argsHint: ${serializeString(command.argsHint)},`);
  }

  lines.push(`  }`);
  return lines.join('\n');
}

function serializeString(value) {
  return `'${value.replaceAll('\\', '\\\\').replaceAll("'", "\\'")}'`;
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
